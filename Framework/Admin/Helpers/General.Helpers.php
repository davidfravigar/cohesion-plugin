class Co_GeneralHelpers
{
	/**
	 * ---------------------------------------------------------------------------
	 * If array key exists
	 * ---------------------------------------------------------------------------
	 */
	public static function inArray($key, $array)
	{
		if(array_key_exists($key, $array)) {
			return true;
		} else {
			return false;
		}
	}
}//end class